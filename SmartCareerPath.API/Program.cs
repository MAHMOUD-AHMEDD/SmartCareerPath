using FluentValidation;
using FluentValidation.AspNetCore;
using Microsoft.AspNetCore.Authentication.JwtBearer;
using Microsoft.AspNetCore.Identity;
using Microsoft.EntityFrameworkCore;
using Microsoft.IdentityModel.Tokens;
using Microsoft.OpenApi.Models;
using SmartCareerPath.Application.Interfaces;
using SmartCareerPath.Application.Validator.Auth;
using SmartCareerPath.Domain.Entites.Identity;
using SmartCareerPath.Infrastructure.Persistence;
using SmartCareerPath.Infrastructure.Services;
using System.Text;

var builder = WebApplication.CreateBuilder(args);

// ?????????????????????????????????????????????
// 1. DATABASE
// ?????????????????????????????????????????????
builder.Services.AddDbContext<AppDbContext>(options =>
    options.UseSqlServer(
        builder.Configuration.GetConnectionString("DefaultConnection")));

// ?????????????????????????????????????????????
// 2. IDENTITY (TPT)
// ?????????????????????????????????????????????
builder.Services.AddIdentity<AppUser, IdentityRole>(options =>
{
    options.Password.RequireDigit = true;
    options.Password.RequiredLength = 8;
    options.Password.RequireUppercase = true;
    options.Password.RequireLowercase = true;
    options.Password.RequireNonAlphanumeric = true;
})
.AddEntityFrameworkStores<AppDbContext>()
.AddDefaultTokenProviders();

// ?????????????????????????????????????????????
// 3. JWT AUTHENTICATION
// ?????????????????????????????????????????????
var jwtSettings = builder.Configuration.GetSection("JwtSettings");
var key = Encoding.UTF8.GetBytes(jwtSettings["Secret"]!);

builder.Services.AddAuthentication(options =>
{
    options.DefaultAuthenticateScheme = JwtBearerDefaults.AuthenticationScheme;
    options.DefaultChallengeScheme = JwtBearerDefaults.AuthenticationScheme;
})
.AddJwtBearer(options =>
{
    options.TokenValidationParameters = new TokenValidationParameters
    {
        ValidateIssuer = true,
        ValidateAudience = true,
        ValidateLifetime = true,
        ValidateIssuerSigningKey = true,
        ValidIssuer = jwtSettings["Issuer"],
        ValidAudience = jwtSettings["Audience"],
        IssuerSigningKey = new SymmetricSecurityKey(key),
        ClockSkew = TimeSpan.Zero
    };
});

builder.Services.AddAuthorization();

// ?????????????????????????????????????????????
// 4. APPLICATION SERVICES
// ?????????????????????????????????????????????
builder.Services.AddScoped<IAuthService, AuthService>();
builder.Services.AddSingleton<JwtTokenService>();

// ?????????????????????????????????????????????
// 5. FLUENTVALIDATION
// ?????????????????????????????????????????????
builder.Services.AddFluentValidationAutoValidation();
builder.Services.AddValidatorsFromAssembly(
    typeof(RegisterSeekerValidator).Assembly);

// ?????????????????????????????????????????????
// 6. CONTROLLERS + SWAGGER
// ?????????????????????????????????????????????
builder.Services.AddControllers();
builder.Services.AddEndpointsApiExplorer();
builder.Services.AddSwaggerGen(c =>
{
    c.SwaggerDoc("v1", new OpenApiInfo
    {
        Title = "Smart Career Path API",
        Version = "v1"
    });

    // Allow Swagger to send Bearer tokens
    c.AddSecurityDefinition("Bearer", new OpenApiSecurityScheme
    {
        Name = "Authorization",
        Type = SecuritySchemeType.ApiKey,
        Scheme = "Bearer",
        BearerFormat = "JWT",
        In = ParameterLocation.Header,
        Description = "Enter: Bearer {your JWT token}"
    });

    c.AddSecurityRequirement(new OpenApiSecurityRequirement
    {
        {
            new OpenApiSecurityScheme
            {
                Reference = new OpenApiReference
                {
                    Type = ReferenceType.SecurityScheme,
                    Id   = "Bearer"
                }
            },
            Array.Empty<string>()
        }
    });
});
//builder.Services.AddSwaggerGen();

// ?????????????????????????????????????????????
// BUILD
// ?????????????????????????????????????????????
var app = builder.Build();

// ?????????????????????????????????????????????
// 7. SEED DATABASE
// ?????????????????????????????????????????????
using (var scope = app.Services.CreateScope())
{
    await DataSeeder.SeedAsync(scope.ServiceProvider);
}

// ?????????????????????????????????????????????
// 8. MIDDLEWARE PIPELINE (ORDER IS CRITICAL)
// ?????????????????????????????????????????????
app.UseMiddleware<ExceptionMiddleware>(); // FIRST Ś wraps everything

if (app.Environment.IsDevelopment())
{
    app.UseSwagger();
    app.UseSwaggerUI();
}

app.UseHttpsRedirection();

app.UseAuthentication(); // SECOND Ś validates JWT, populates User claims
app.UseAuthorization();  // THIRD  Ś enforces [Authorize] attributes

app.MapControllers();    // LAST

app.Run();

